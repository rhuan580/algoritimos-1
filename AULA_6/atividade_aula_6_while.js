// 1 - Obrigatoriamente utilizando o laço de repetição "while" faça com que um valor chamado "Contador" vá de 0 até 10, imprimindo cada vez que o valor incrementar.
// 2 - Crie uma variável chamada contador e atribua o valor 0;
// 3 - Escreva um WHILE
// Obs: A tradução de WHILE significa ENQUANTO.
// 4 - Vamos utilizar um operador "<" que é MENOR QUE.

let contador = 0;
while (contador <= 10) {
  console.log("Valor do contador:", contador);
  console.log("Ainda é menor que 10:");
  contador++;
}
